// Switch 도 조건문에 해당한다. Conditional Statement

// 정해진 범위안의 값에 대해 특정한 일을 해야하는 경우
let day = 6;
let dayName;

switch(day) {
    case 0:
        dayName = 'Monday'
        break;
    case 1:
        dayName = 'Tuesday'
        break;
    case 2:
        dayName = 'Wednesday'
        break;
    case 3:
        dayName = 'Thursday'
        break;
    case 4:
        dayName = 'Friday'
        break;
    case 5:
        dayName = 'Saturday'
        break;
    case 6:
        dayName = 'Sunday'
        break;
}